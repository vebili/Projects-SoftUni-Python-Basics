# Изходни множества:
A={1,2}
B={1,2,3}
C={3,2,1}
# Съдържание на множествата:
print("A =",A)
print("B =",B)
print("C =",C)
# Сравнение на множествата:
print("A==B:",A==B)
print("A!=B:",A!=B)
print("B==C:",B==C)
print("B!=C:",B!=C)
print("A<B:",A<B)
print("A>B:",A>B)
print("B<C:",B<C)
print("B<=C:",B<=C)
print("B>=C:",B>=C)
print("B>=A:",B>=A)
