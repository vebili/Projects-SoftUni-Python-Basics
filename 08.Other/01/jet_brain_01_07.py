# Прочитане на цяло число:
number = int(input("Въведете число: "))
# Ако числото е четно:
if number % 2 == 0:
    print("Вие въведохте четно число.")
# Ако числото е нечетно:
else:
    print("Вие въведохте нечетно число.")

if number % 2 == 0:
    print(f"Вашето число е: {number}")
# Ако числото е нечетно:
else:
    print(f"Вашето число е: {number}")
