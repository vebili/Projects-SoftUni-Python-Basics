# Отваряме текстовия файл за четене:
mf = open("Н:\\Book\\Python\\poetry.txt")
# Променлива брояч на редове:
k = 1
# Четене на файла ред по ред:
print("Четене на файла ред по ред")
for L in mf:
    # Показване на номера на реда и самия ред:
    print("[" + str(k) + "]", L, end="")
    # Нова стойност за номера на реда:
    k += 1
# Затваряме файла:
mf.close()
print("\nФайлът е затворен...")
