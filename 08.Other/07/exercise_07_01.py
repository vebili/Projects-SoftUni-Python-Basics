print("Числа, зададени в различни бройни системи:")
# Числото 19 в двоична система:
A=0b10011
print("A =",A)
# Числото 93 в осмична система:
B=0o135
print("B =",B)
# Числото 2603 в шестнайсетична система:
C=0xA2B
print("C =",C)
print("C-A-B =",C-A-B)
# Обратно преобразуване:
print("Обратно преобразуване:")
A=int("10011",2)
print(bin(A),"=",A)
B=int("0o135",8)
print(oct(B),"=",B)
C=int("0xA2B",16)
print(hex(C),"=",C)
D=int("ABC",20)
print("ABC =",D)
