# Описание на класа:
class MyClass:
    pass
# Създаване на обекти на основата на класа:
A=MyClass()
B=MyClass()
# Обекти:
print("Обект A:",A)
print("Обект B:",B)
# Тип на обектите:
print("Клас на обект A:",type(A).__name__)
print("Клас на обект B:",type(B).__name__)
# Сравнение на обекти:
print("A==B:",A==B)
