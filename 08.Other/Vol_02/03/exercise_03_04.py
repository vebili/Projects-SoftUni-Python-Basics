# Създаване на обект на изключението:
error=Exception("Първа грешка")
# Първи блок с контролируем код:
try:
    # Втори блок с контролируем код:
    try:
        # Трети блок с контролируем код:
        try:
            # Генериране на изключение:
            raise error
        # Обработка на изключението за третия блок:
        except:
            print(error)
            # Повторно генериране на изключение:
            raise
    # Обработка на изключението за втория блок:
    except Exception as e:
        print("Повторна обработка:")
        print(e)
        # Вътрешен блок с контролируем код:
        try:
            # Генериране на изключение:
            raise ArithmeticError("Втора грешка")
        # Обработка на изключението за вътрешния блок:
        except ArithmeticError as e:
            print(e)
        # Генериране на изключение:
        raise Warning
# Обработка на изключението за първия блок:
except Warning:
    print("Друга грешка")
