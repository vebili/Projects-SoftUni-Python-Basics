# Импортиране на функцията за създаване на пълно копие:
from copy import deepcopy

# Изходен списък:
A = [1, 3, [10, 20], "Python", [40, 50]]
# Създаване на пълно копие на списъка:
B = deepcopy(A)
print("Изходни стойности:")
print("A:", A)
print("B:", B)
# Внасяне на изменения в изходния списък:
A[0] = [100, 200]
A[2][1] = 300
A[3] = "Java"
A[4] = 90
print("След измененията:")
print("A:", A)
print("B:", B)
