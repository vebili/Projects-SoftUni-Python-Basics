# Изходен текст:
txt=input("Въведете текст: ")
# Нов текст:
new_txt=""
# Индекс на символа:
num=0
# Обхождане на символите:
while num<len(txt)-1:
    # Добавяне на символ в текста:
    new_txt+=txt[num+1]+txt[num]
    # Нова стойност на индекса:
    num+=2
# Ако в изходния текст е останал символ:
if num<len(txt):
    new_txt+=txt[num]
# Показване на резултата:
print("Резултат:",new_txt)
